// Получение теста по ID
export const getTestById = (id: string): Test | undefined => {
  return tests.find((test) => test.id === id);
};

// Получение следующего теста
export const getNextTest = (currentTestId: string): Test | undefined => {
  const currentIndex = tests.findIndex((test) => test.id === currentTestId);
  if (currentIndex === -1 || currentIndex === tests.length - 1) {
    return undefined;
  }
  return tests[currentIndex + 1];
};

// Получение предыдущего теста
export const getPrevTest = (currentTestId: string): Test | undefined => {
  const currentIndex = tests.findIndex((test) => test.id === currentTestId);
  if (currentIndex <= 0) {
    return undefined;
  }
  return tests[currentIndex - 1];
};
